#!/usr/bin/env python3
"""
crop_math_115A.py — 115 數學A 每題裁圖（題幹＋選項一起）。

數學公式無法可靠文字化，依規範每題裁官方 PDF 為圖，答題卡另放作答輸入。
裁圖座標為 (page_index, top, bottom)，x 固定 50–548，200 DPI。
改座標後重跑即可覆蓋 public/images/115學年度/數學A/qNN.png。

⚠️ 待修正（2026-07-10 傑克驗收，等非選擇題方案確定後一起改）：
- Q5 (2,96,273)  ：第一行矩陣頂端被裁掉 → top 需往上（約 88），並確認未吃到頁首。
- Q6 (2,273,446)：底部吃到「二、多選題」區段標題 → bottom 需往上（約 405）。
- Q10(3,445,793)：底部留白太多 → bottom 收到內容結束處。
- Q11(4,87,438) ：底部留白太多 → bottom 收到內容結束處。
- Q12(4,438,793)：底部留白太多 → bottom 收到內容結束處。
- Q14(5,314,409)：題目有部分被裁 → 依附件截圖重抓上下界。
（其餘 Q1-4,7-9,13,15-18 驗收 OK。Q13 已收到 293、Q16 已收到 784。）
"""
import glob
import fitz

PDF = glob.glob("資料/大考中心官方PDF/115學年度/數學A/*試卷*.pdf")[0]
OUT = "public/images/115學年度/數學A"

# (page_index, top, bottom)；x 固定 50–548
BOXES = {
    1: (1, 153, 265), 2: (1, 265, 434), 3: (1, 434, 589), 4: (1, 589, 793),
    5: (2, 96, 273),  6: (2, 273, 446), 7: (2, 446, 592), 8: (2, 592, 793),
    9: (3, 84, 445),  10: (3, 445, 793),
    11: (4, 87, 438), 12: (4, 438, 793),
    13: (5, 128, 293), 14: (5, 314, 409), 15: (5, 409, 590), 16: (5, 590, 784),
    17: (6, 84, 300), 18: (6, 410, 566),
}


def main():
    doc = fitz.open(PDF)
    z = fitz.Matrix(200 / 72, 200 / 72)
    for n, (pi, top, bot) in BOXES.items():
        doc[pi].get_pixmap(matrix=z, clip=fitz.Rect(50, top, 548, bot)).save("%s/q%02d.png" % (OUT, n))
    print("cropped %d questions -> %s" % (len(BOXES), OUT))


if __name__ == "__main__":
    main()
